# Integer
integer_value = 98
print(f"Integer value: {integer_value}")
# Alternatively it can be printed by converting it to string.
print("Integer value: " + str(integer_value))

# Float
float_value = 22.0/7
print(f"Float value: {float_value}")

# String
string_value = "Hello, World!"
print(f"String value: {string_value}")

# Boolean
boolean_value = True
print(f"Boolean value: {boolean_value}")
